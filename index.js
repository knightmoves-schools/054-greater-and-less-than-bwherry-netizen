function oldEnough (age){
    if (age > 17) {
        return "old enough";
    }

    if (age <= 17) {
        return null;
    }
}


function underAge (age){
    if (age < 18) {
        return "under age";
    }

    if (age >= 18) {
        return null;
    }
}

